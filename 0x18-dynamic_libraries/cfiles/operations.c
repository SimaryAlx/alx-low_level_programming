#include <stdio.h>

/**
* add - calculates and add two numbers.
* @a: firist number
* @b: second number
*
* Return: addition.
*/
int add(int a, int b)
{
return (a + b);
}

/**
* sub - calculates difference and subtract two numbers.
* @a: firist number
* @b: second number
*
* Return: subtracted final difference result.
*/
int sub(int a, int b)
{
return (a - b);
}

/**
* mul - multiply and calculates product of two numbers
* @a: firist number
* @b: second number
*
* Return: product.
*/
int mul(int a, int b)
{
return ((a) * (b));
}

/**
* div - divides and calculates the quotient of two numbers.
* @a: firist number
* @b: second number
*
* Return: division or quotient result.
*/
int div(int a, int b)
{
return ((a) / (b));
}

/**
* mod - calculates teh modulus of two numbers
* @a: firist number
* @b: second number
*
* Return: final result of modulus.
*/
int mod(int a, int b)
{
return ((a) % (b));
}
